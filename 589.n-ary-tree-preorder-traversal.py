# @lc code=start
"""
# Definition for a Node.
class Node:
    def __init__(self, val: Optional[int] = None, children: Optional[List['Node']] = None):
        self.val = val
        self.children = children
"""

class Solution:
    def preorder(self, root: 'Node') -> List[int]:
        if not root:
            return []
        res = []

        def through(node: 'Node'):
            res.append(node.val)
            for c in node.children:
                through(c)

        through(root)
        return res 
# @lc code=end

